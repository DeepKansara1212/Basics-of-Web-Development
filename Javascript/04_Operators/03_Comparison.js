// > < >= <= == != === !==

let a = 5;
let b = 20;
let c = 5;
let d = "5";

console.log(a > b);
console.log(a < b);
console.log(a <= b);
console.log(a >= b);

// '==' operator only checks for value equality
console.log(a == c);
console.log(a != c);

// '===' operator checks for both value and type equality
console.log(a === d);
console.log(a !== d);
